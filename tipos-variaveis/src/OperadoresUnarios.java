public class OperadoresUnarios {
    public static void main(String[] args) {
        
        int numero = 5;

        numero++; //repetição com incremento.
                  // numero++ é a mesma coisa que dizer numero = numero + 1;
        System.out.println(numero);


        //BOOLEANO:

        boolean variavel = true;

        variavel = !variavel; //eu declarei a variavel como true e para inverter,
                                //utilizo o ! 

        System.out.println(variavel);
    }
}
